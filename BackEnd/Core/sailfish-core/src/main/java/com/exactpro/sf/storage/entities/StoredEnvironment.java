package com.exactpro.sf.storage.entities;

import java.util.Set;

public class StoredEnvironment implements Cloneable {
	
	private Long id;
	
	private String name;

    private String variableSet;
	
	private Set<StoredService> services;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

    public String getVariableSet() {
        return variableSet;
    }

    public void setVariableSet(String variableSet) {
        this.variableSet = variableSet;
    }

    public Set<StoredService> getServices() {
		return services;
	}

	public void setServices(Set<StoredService> services) {
		this.services = services;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) return true;
		if (obj == null) return false;
		if (getClass() != obj.getClass()) return false;
		StoredEnvironment other = (StoredEnvironment) obj;
		if (id == null) {
			if (other.id != null) return false;
		} else if (!id.equals(other.id)) return false;
		return true;
	}
	
	@Override
	public StoredEnvironment clone() {
		StoredEnvironment env = new StoredEnvironment();
		env.setName(this.name);
        env.setVariableSet(variableSet);
		return env;
	}

}
