package com.exactpro.sf.embedded.statistics.entities;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

@SuppressWarnings("serial")
@Entity
@Table(name = "sttestcases", uniqueConstraints = @UniqueConstraint(columnNames = "testcaseid"))
@SequenceGenerator(name="sttestcases_generator", sequenceName="sttestcases_sequence")
public class TestCase implements Serializable {
	
	@Id
	@GeneratedValue(strategy=GenerationType.AUTO, generator="sttestcases_generator")
	private Long id;
	
	private String testCaseId;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getTestCaseId() {
		return testCaseId;
	}

	public void setTestCaseId(String testCaseId) {
		this.testCaseId = testCaseId;
	}

	@Override
	public int hashCode() {
        int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
        if(this == obj) {
            return true;
        }
        if(obj == null) {
            return false;
        }
        if(getClass() != obj.getClass()) {
            return false;
        }
		TestCase other = (TestCase) obj;
		if (id == null) {
            if(other.id != null) {
                return false;
            }
        } else if(!id.equals(other.id)) {
            return false;
        }
		return true;
	}
	
}
